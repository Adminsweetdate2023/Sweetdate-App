function BottomTabsRoot({ navigation }: any) {
  const [bottomTabItemsNormal] = React.useState([
    <Nav11 />,
    <Nav6 />,
    <Live2 />,
    <Nav4 />,
    <Nav5 />,
  ]);
  const [bottomTabItemsActive] = React.useState([
    <Nav1 />,
    <Nav61 />,
    <Live />,
    <Nav41 />,
    <Nav51 />,
  ]);
  return (
    <Tab.Navigator
      screenOptions={{ headerShown: false }}
      tabBar={({ state, descriptors, navigation }: any) => {
        const activeIndex = state.index;
        return (
          <View
            style={{
              backgroundColor: "#fff",
              width: 360,
              flexDirection: "row",
              paddingHorizontal: 5,
              paddingBottom: 10,
              alignItems: "center",
              justifyContent: "space-between",
              height: 60,
            }}
          >
            {bottomTabItemsNormal.map((item: any, index: any) => {
              const isFocused = state.index === index;
              return (
                <Pressable
                  key={index}
                  onPress={() => {
                    navigation.navigate({
                      name: state.routes[index].name,
                      merge: true,
                    });
                  }}
                >
                  {activeIndex === index
                    ? bottomTabItemsActive[index] || item
                    : item}
                </Pressable>
              );
            })}
          </View>
        );
      }}
    >
      <Tab.Screen
        name="Photo220"
        component={Photo220}
        options={{ headerShown: false }}
      />
      <Tab.Screen
        name="Photo268"
        component={Photo268}
        options={{ headerShown: false }}
      />
      <Tab.Screen
        name="Live"
        component={LiveIcon}
        options={{ headerShown: false }}
      />
      <Tab.Screen
        name="Photo287"
        component={Photo287}
        options={{ headerShown: false }}
      />
      <Tab.Screen
        name="Photo26"
        component={Photo26}
        options={{ headerShown: false }}
      />
    </Tab.Navigator>
  );
}

